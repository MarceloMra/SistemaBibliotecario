    private void selecionar(){
        try{
            String id_biblio  = tbResultado.getValueAt(tbResultado.getSelectedRow(), 0).toString();
            int id = Integer.parseInt(id_biblio);
            
            for(Bibliotecaria b : resultado){
                if(b.getId() == id){
                    ((Dependente) parent).setInformacaoDependente(b);
                    parent.setEstadoAtivacao(true);
                    this.dispose();
                    break;
                }
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Erro ao tentar converter o id para int!", "Erro na convers√£o", JOptionPane.ERROR_MESSAGE);
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Voc√™ precisa selecionar uma linha da tabela!", "Selecione uma linha", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void buscar() {
        if (!txtConteudoBuscar.getText().equals("")) {
            resultado.clear();
            switch (cbTipoBusca.getSelectedIndex()) {
                case 0:
                    //ID
                    resultado = bb.buscarBibliotecaria(txtConteudoBuscar.getText(), 0);
                    break;
                case 1:
                    //Nome
                    resultado = bb.buscarBibliotecaria(txtConteudoBuscar.getText(), 1);
                    break;
                case 2:
                    //CPF
                    resultado = bb.buscarBibliotecaria(txtConteudoBuscar.getText(), 2);
                    break;
                case 3:
                    //Email
                    resultado = bb.buscarBibliotecaria(txtConteudoBuscar.getText(), 3);
                    break;
                default:
                    break;
            }

            DefaultTableModel dtbm = (DefaultTableModel) tbResultado.getModel();
            while (dtbm.getRowCount() > 0) {
                dtbm.removeRow(0);
            }

            int i = 0;

            for (Bibliotecaria b : resultado) {
                dtbm.addRow(new String[i]);
                tbResultado.setValueAt(b.getId(), i, 0);
                tbResultado.setValueAt(b.getNome(), i, 1);
                tbResultado.setValueAt(b.getCpf(), i, 2);
                tbResultado.setValueAt(b.getEmail(), i, 3);
                i++;
            }
        } else {
            txtConteudoBuscar.requestFocus();
            JOptionPane.showMessageDialog(null, "Preencha o campo de busca para prosseguir com a a√ß√£o!", "Campo vazio", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void inicializarTabela() {
        resultado.clear();
        resultado = bb.listarBibliotecarios();
        
        DefaultTableModel dtbm = (DefaultTableModel) tbResultado.getModel();
        while (dtbm.getRowCount() > 0) {
            dtbm.removeRow(0);
        }

        int i = 0;

        for (Bibliotecaria b : resultado) {
            dtbm.addRow(new String[i]);
            tbResultado.setValueAt(b.getId(), i, 0);
            tbResultado.setValueAt(b.getNome(), i, 1);
            tbResultado.setValueAt(b.getCpf(), i, 2);
            tbResultado.setValueAt(b.getEmail(), i, 3);
            i++;
        }
    }

    private void restaurarTela() {
        txtConteudoBuscar.setText("");
        cbTipoBusca.setSelectedIndex(0);
        tbResultado.removeAll();
        resultado.clear();
    }

