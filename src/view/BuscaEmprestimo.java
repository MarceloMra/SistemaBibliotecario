    private void selecionar(){
        try{
            String id_exemp  = tbResultado.getValueAt(tbResultado.getSelectedRow(), 0).toString();
            int id = Integer.parseInt(id_exemp);
            
            for(ItemBuscaEmprestimo b : resultado){
                if(b.getIdEmprestimo()== id){  
                    ((Dependente) parent).setInformacaoDependente(b);
                    parent.setEstadoAtivacao(true);
                    this.dispose();
                    break;
                }
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Erro ao tentar converter o id para int!", "Erro na conversão", JOptionPane.ERROR_MESSAGE);
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Você precisa selecionar uma linha da tabela!", "Selecione uma linha", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    private void buscar(){
        if (!txtConteudoBuscar.getText().equals("")) {
            resultado.clear();
            resultado = be.buscarItemEmprestimo(txtConteudoBuscar.getText(), cbTipoBusca.getSelectedIndex());
            
            DefaultTableModel dtbm = (DefaultTableModel) tbResultado.getModel();
            while (dtbm.getRowCount() > 0) {
                dtbm.removeRow(0);
            }

            int i = 0;

            for (ItemBuscaEmprestimo b : resultado) {
                dtbm.addRow(new String[i]);
                tbResultado.setValueAt(b.getIdEmprestimo(), i, 0);
                tbResultado.setValueAt(b.getCodBarras(), i, 1);
                tbResultado.setValueAt(b.getTitulo(), i, 2);
                tbResultado.setValueAt(b.getNomeUsuario(), i, 3);
                tbResultado.setValueAt(b.getDataEmprestimo(), i, 4);
                tbResultado.setValueAt(b.getDataPrevistaDevol(), i, 5);
                if(b.getDataDevolucao().equals("11/11/1111")){
                    tbResultado.setValueAt("", i, 6);
                }else{
                    tbResultado.setValueAt(b.getDataDevolucao(), i, 6);
                }
                i++;
            }
        } else {
            txtConteudoBuscar.requestFocus();
            JOptionPane.showMessageDialog(null, "Preencha o campo de busca para prosseguir com a ação!", "Campo vazio", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void inicializarTabela(){
        resultado.clear();
        resultado = be.listarEmprestimos();
        
        DefaultTableModel dtbm = (DefaultTableModel) tbResultado.getModel();
        while (dtbm.getRowCount() > 0) {
            dtbm.removeRow(0);
        }

        int i = 0;

        for (ItemBuscaEmprestimo b : resultado) {
            dtbm.addRow(new String[i]);
            tbResultado.setValueAt(b.getIdEmprestimo(), i, 0);
            tbResultado.setValueAt(b.getCodBarras(), i, 1);
            tbResultado.setValueAt(b.getTitulo(), i, 2);
            tbResultado.setValueAt(b.getNomeUsuario(), i, 3);
            tbResultado.setValueAt(b.getDataEmprestimo(), i, 4);
            tbResultado.setValueAt(b.getDataPrevistaDevol(), i, 5);
            if(b.getDataDevolucao().equals("11/11/1111")){
                tbResultado.setValueAt("", i, 6);
            }else{
                tbResultado.setValueAt(b.getDataDevolucao(), i, 6);
            }
            i++;
        }
    }
    
